public class Text {

    public int X;
    public int Y;
    public String TEXT;
    public int SIZE;
    public String font;

    public Text(int x, int y, String text, int size, String font){
        this.X = x;
        this.Y = y;
        this.TEXT = text;
        this.SIZE = size;
        this.font = font;
    }
}
